  function extractName(message) {
    let msg = message.toLowerCase();

    const namePatterns = [
      /my name is ([a-zA-Z ]+)/i,
      /i am ([a-zA-Z ]+)/i,
      /i'm ([a-zA-Z ]+)/i,
      /me ([a-zA-Z ]+)/i,
      /this is ([a-zA-Z ]+)/i,
      /([a-zA-Z ]+) is my name/i,
      /you can call me ([a-zA-Z ]+)/i,
      /myself ([a-zA-Z ]+)/i,
      /([a-zA-Z ]+) here/i,
      /^([a-zA-Z ]+)$/i
    ];

    for (let pattern of namePatterns) {
      const match = message.match(pattern);
      if (match && match[1]) {
        return match[1].trim().replace(/[^a-zA-Z ]/g, '');
      }
    }
    return null;
  }

